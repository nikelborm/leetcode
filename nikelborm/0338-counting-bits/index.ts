function countBits(n: number): number[] {
  let res: number[] = [0, 1];
  const iterations = Math.floor(Math.log2(n));
  for (let i = 0; i < iterations; i++) {
    res.push(...res.map(e => e + 1))
  }
  return res.slice(0, n + 1)
};


